# Objetivo: Converter uma medição em graus celsius para fahrenheit
temp_celsius <- 72 # kilometros

# Precisamos modificar os valores para metro / segundo
temp_fahrenheit <- ((9 * temp_celsius) + 160) / 5

cat("A conversão resultou em: ", temp_fahrenheit, "F")